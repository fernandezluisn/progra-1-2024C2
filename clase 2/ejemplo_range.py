# For de 0 a 4 usando range
for numero in range(5):
    print(f"El número es {numero}")

# For de 1 a 5 usando range
for numero in range(1,6):
    print(f"El número es {numero}")

# mostrar números impares que hay entre 1 a 10 ysabdi range
for numero in range(1,10,2):
    print(f"El número es {numero}")

print(f"La función range devuelve un objeto de tipo {type(range(1,10,2))}")

